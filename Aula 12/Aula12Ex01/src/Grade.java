    private void jToggleButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton6ActionPerformed
        // TODO add your handling code here:
        if(jTableDados.getSelectionModel().isSelectionEmpty()){
            JOptionPane.showMessageDialog(null, "Selecione uma linha, por favor!!!!");
        } else {
            int i = jTableDados.getSelectedRow();
            String selecionado = jTableDados.getModel().getValueAt(i, 0) + "\n" +
                                jTableDados.getModel().getValueAt(i, 1);
            JOptionPane.showMessageDialog(null, selecionado);
        }
    }//GEN-LAST:event_jToggleButton6ActionPerformed

