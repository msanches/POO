    private void jToggleButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton7ActionPerformed
        // TODO add your handling code here:
        
        String infos = "";
        infos += "Q linhas: " + jTableDados.getModel().getRowCount() + "\n";
        infos += "Q colunas: " + jTableDados.getModel().getColumnCount() + "\n";
        infos += "Linha selecionada: " + jTableDados.getSelectedRow() + "\n";
        infos += "Coluna selecionada: " + jTableDados.getSelectedColumn() + "\n";
        
        if(jTableDados.getModel().getRowCount() > 0){
            infos += "Nome das colunas " + jTableDados.getColumnName(0) + " - " +
                    jTableDados.getColumnName(1);
        }
        
        JOptionPane.showMessageDialog(null, infos);
    }//GEN-LAST:event_jToggleButton7ActionPerformed

    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        String dados = "";
        for (int i = 0; i < jTableDados.getModel().getRowCount(); i++) {
            dados += "Linha: " + i + " ";
            for (int j = 0; j < jTableDados.getModel().getColumnCount(); j++) {
                dados += " " + jTableDados.getModel().getValueAt(i, j);
            }
            dados += "\n";
        }
        JOptionPane.showMessageDialog(null, dados);
    }//GEN-LAST:event_jToggleButton1ActionPerformed

